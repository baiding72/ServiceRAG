DEFAULT_RESPONSE = "您好，您的问题已收到，请您耐心等待处理结果，谢谢。"


def search_knowledge(question: str) -> Tuple[str, Optional[List[str]]]:
    """
    根据问题检索知识库

    Args:
        question: 用户的问题文本

    Returns:
        Tuple[str, Optional[List[str]]]: (回复文本, 图片ID列表)
            - 如果命中图文规则，返回文本和图片列表
            - 如果命中普通文本规则，返回文本和 None
            - 如果未命中，返回默认回复和 None
    """
    # 优先匹配图文规则
    for keyword, (text, images_json) in KNOWLEDGE_BASE.items():
        if keyword in question:
            # 解析图片ID列表
            import json
            try:
                images = json.loads(images_json)
            except json.JSONDecodeError:
                images = []
            return text, images

    # 其次匹配普通文本规则
    for keyword, text in TEXT_RESPONSES.items():
        if keyword in question:
            return text, None

    # 未命中任何规则，返回默认回复
    return DEFAULT_RESPONSE, None


def format_answer(text: str, images: Optional[List[str]] = None) -> str:
    """
    格式化最终答案

    Args:
        text: 回复文本
        images: 图片ID列表（可选）

    Returns:
        str: 格式化后的答案字符串
    """
    if images:
        # 按照比赛要求的格式拼接：文本 + 图片数组
        import json
        return f'"{text}",{json.dumps(images, ensure_ascii=False)}'
    return text
